void main() {
  var bil1 = 10;
  var bil2 = 10;

  print(bil1 + bil2); // hasil 20
  print(bil1 - bil2); // hasil 0
  print(bil1 * bil2); // hasil 100
  print(bil1 /
      bil2); // hasil 1.0 karena untuk operator ini hasilnya adalah double
  print(bil1 ~/ bil2); // hasul 1 karena unutk operator ini hasilnya integer
  print(bil1 % bil2); // hasil 0
}
